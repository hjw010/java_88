package ch01;

public class _04_VariableEx {

	
	public static void main(String[] args) {
		// p 52	
		char ch1 = 'A';
		System.out.println("ch1 : " + ch1);
		//문자에 해당하는 정수값(아스키코드값) 출력 = > 인코딩 (문자->숫자) 
		System.out.println("인코딩 : " + (int)ch1);
		
		
		//정수값(아스키코드값)에 해당하는 문자 출력 = > 디코딩(숫자->문자)
		char ch2 = 66;
		System.out.println("디코딩 : " + ch2);
		
		int ch3 = 67; 
		System.out.println(ch3);
		System.out.println((char)ch3);
		
		//A = 65, Z = 90 
		//a = 97, z = 122
	
		
		
		
		
		System.out.println("*******************************");
		
		char ch4 = 'a';
		System.out.println(ch4);
		//문자에 해당하는 정수값(아스키코드값) 출력 = > 인코딩 (문자->숫자) 
		System.out.println((int)ch4);
		
		
		char ch5 = 'z';
		System.out.println(ch5);
		//문자에 해당하는 정수값(아스키코드값) 출력 = > 인코딩 (문자->숫자) 
		System.out.println((int)ch5);
		
		
		
		
		
		
		
		
		
		
		
		
		
	
	}
	
	
}
